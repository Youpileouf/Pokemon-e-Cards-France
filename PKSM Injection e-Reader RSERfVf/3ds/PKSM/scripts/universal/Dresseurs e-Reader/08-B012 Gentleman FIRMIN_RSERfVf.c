int main(int argc, char **argv)
{
    unsigned char version;
    char *data;
    int offset;
    int choice;
    char *menu[3];
    
    /* Déterminer la version du jeu */
    version = *argv[0];
    
    /* Menu simple */
    menu[0] = "Quitter";
    menu[1] = "08-B012 Gentleman FIRMIN";
    menu[2] = "Effacer Dresseur de la .sav";

    while (1)
    {
        choice = gui_menu_20x2("Dresseur e-Reader", 3, menu);
        
        if (choice == 0)
            return 0;
        
        /* Sélectionner les données selon le choix et la version */
        if (choice == 1) {
            /* DEVIN */
            if (version == 3)
                data = (char*)gentleman_firmin_E;
            else if (version == 4)
                data = (char*)gentleman_firmin_RSRfVf;
            else
                data = (char*)gentleman_firmin_RSRfVf;
        } else {
            /* Effacer la donner présente dans le .sav */
            if (version == 3)
                data = (char*)nettoyer_slot;
            else if (version == 4)
                data = (char*)nettoyer_slot;
            else
                data = (char*)nettoyer_slot;
        }
        
        /* Déterminer l'offset selon la version */
        if (version == 1)
            offset = 0x498;      /* Rubis/Saphir */
        else if (version == 3)
            offset = 0xBEC;      /* Émeraude */
        else if (version == 4)
            offset = 0x4A0;      /* Rouge Feu/Vert Feuille */
        else
            return 1;
        
        /* Injecter les données */
        sav_set_data(data, 188, 0, offset);
        
        /* Demander si on continue */
        if (!gui_choice("Injecter un autre\ndresseur ?"))
            break;
    }
    
    return 0;
}




























