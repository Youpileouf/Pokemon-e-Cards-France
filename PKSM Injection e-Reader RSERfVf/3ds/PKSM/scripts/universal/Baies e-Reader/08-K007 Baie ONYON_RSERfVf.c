int main(int argc, char **argv)
{
    unsigned char version;
    char *data;
    int offset;
    int datasize;
    int choice;
    char *menu[3];
    
    /* Déterminer la version du jeu */
    version = *argv[0];
    
    /* Menu simple */
    menu[0] = "Quitter";
    menu[1] = "08-K007 Baie ONYON";
    menu[2] = "Effacer baie de la .sav";

    while (1)
    {
        choice = gui_menu_20x2("Baie e-Reader", 3, menu);
        
        if (choice == 0)
            return 0;
        
        /* Sélectionner les données selon le choix et la version */
        if (choice == 1) {
            /* ONYON */
            if (version == 3){
                data = (char*)baie_onyon_ERfVf;
		sav_set_byte(0x01, 2, 0x476);//active var_enigma_berry_available pour première distribution de la baie
		}
            else if (version == 4)
                data = (char*)baie_onyon_ERfVf;
            else{
                sav_set_data(nettoyer_slot_RS, 1328, 4, 0x2E0);
                sav_set_data(baie_onyon_RS1, 32, 4, 0x2E0);//injection par morceaux pour gagner de la place
                sav_set_data(baie_onyon_RS2, 48, 4, 0x350);//et éviter le Variable Alloc out of memory error
                sav_set_data(baie_onyon_RS3, 80, 4, 0x3F0);
		sav_set_data(baie_onyon_RS4, 128, 4, 0x4A0);
		sav_set_data(baie_onyon_RS5, 320, 4, 0x550);
		sav_set_data(baie_onyon_RS6, 48, 4, 0x6F0);
		sav_set_data(baie_onyon_RS7, 160, 4, 0x770);
		sav_set_byte(0x01, 2, 0x41A);//active var_enigma_berry_available pour première distribution de la baie
                }
        } else {
            /* Effacer la donner présente dans le .sav */
            if (version == 3){
                data = (char*)nettoyer_slot_ERfVf;
		sav_set_byte(0x00, 2, 0x476);//reset var_enigma_berry_available pour première distribution de la baie
		}
            else if (version == 4)
                data = (char*)nettoyer_slot_ERfVf;
            else{
                sav_set_data(nettoyer_slot_RS, 1328, 4, 0x2E0);
		sav_set_byte(0x00, 2, 0x41A);//reset var_enigma_berry_available pour première distribution de la baie
		}
        }
        
        /* Déterminer l'offset selon la version */
        if (version == 1){
            offset = 0x2E0;      /* Rubis/Saphir */
        }else if (version == 3){
            offset = 0x0378;      /* Émeraude */
            datasize = 52;
            sav_set_data(data, datasize, 4, offset);
        }else if (version == 4){
            offset = 0x26C;      /* Rouge Feu/Vert Feuille */
            datasize = 52;
            sav_set_data(data, datasize, 4, offset);
        }else
            return 1;
        
        /* Demander si on continue */
        if (!gui_choice("Injecter une autre\nbaie ?"))
            break;
    }
    
    return 0;
}
